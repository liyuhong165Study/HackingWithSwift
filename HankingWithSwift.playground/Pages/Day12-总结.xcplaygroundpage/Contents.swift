 ## 选项摘要
 
 您已经完成了本系列的第十部分，让我们总结一下：

 Optionals 让我们以清晰明确的方式表示值的缺失。
 Swift 不会让我们在没有解开它们的情况下使用可选项，无论是使用if let还是使用guard let.
 您可以使用感叹号强制解包选项，但如果您尝试强制解包，nil您的代码将崩溃。
 隐式解包的选项没有常规选项的安全检查。
 如果里面没有任何东西，您可以使用 nil 合并来解开可选项并提供默认值。
 可选链让我们可以编写代码来操作一个可选项，但是如果可选的结果是空的，代码就会被忽略。
 您可以使用try?将抛出函数转换为可选的返回值，或者try!在抛出错误时崩溃。
 如果您需要初始化程序在输入错误时失败，请使用init?()来创建一个可失败的初始化程序。
 您可以使用类型转换将一种类型的对象转换为另一种类型。

