 ## 协议和扩展摘要
 
 您已经完成了本系列的第九部分，让我们总结一下：

 协议描述了符合标准的类型必须具有哪些方法和属性，但不提供这些方法的实现。
 您可以在其他协议之上构建协议，类似于类。
 扩展允许您将方法和计算属性添加到特定类型，例如Int.
 协议扩展允许您向协议添加方法和计算属性。
 面向协议的编程是将应用程序架构设计为一系列协议，然后使用协议扩展来提供默认方法实现的实践。


 
